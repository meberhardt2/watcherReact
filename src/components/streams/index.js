import React, { useState, useEffect } from 'react';

import API from 'api/api';

/**************************************************************************************/
function Streams(){

    /****************************************/
    const [streams, setStreams] = useState([]);
	/****************************************/


	/****************************************/
    useEffect(() => {
        API.getStreams().then((data) => {
            console.log(data);
            setStreams(data);
        });
    }, []);
	/****************************************/


    /****************************************/
    return(
        <div>saved here</div>
    )
	/****************************************/
}
/**************************************************************************************/


export default Streams;